class threevarope{//three variable operation
    public static void main(String[] args){
        int a=10;
        int b=15;
        int c=20;
        int t1,t2,t3;
        t1=a;
        t2=b;
        t3=c;
        b=t1;
        c=t2;
        a=t3;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}